import type { GetServerSideProps, NextPage } from 'next';
import Head from 'next/head';
import { useEffect, useState } from 'react';
import { API_URL } from '../utils/paths';

import { Layout, Space, Button, Input, Typography, Row, Col, Divider, Modal } from 'antd';
import InvoiceCard from '../components/invoice-card';

const { Header, Footer, Sider, Content } = Layout;
const { Title } = Typography;

interface ITestData {
  id: number,
  name: string,
  account: string,
}

const Home: NextPage<IProps> = ({api}) => {
  const [test, setTest] = useState<ITestData[]>([]);
  const [count, setCount] = useState(1);
  const [addModalVisible, setAddModalVisible] = useState(false);

  const doQuery = (count: number) => {
    fetch(`http://${api}?count=${count}`)
      .then(res => res.json())
      .then(json => {
        console.info(json);
        setTest(json)
      })
      .catch(err => console.error(err))
  }

  useEffect(() => {
    doQuery(1);
  }, []);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <Header>
          <Space>
            <Title style={{color: 'white'}}>
              { `Invoice - ${api}` }
            </Title>
          </Space>
        </Header>
        <Content>
          <Layout style={{marginTop: '1rem', marginLeft: '1rem', marginRight: '1rem'}}>
            <Space>
              <Input 
                type="number" 
                value={count}
                onChange={e => setCount(e.currentTarget.valueAsNumber)}
              />
              <Button
                type="primary"
                onClick={() => doQuery(count)}
              >
                Get Invoices
              </Button>
              <Button
                type="default"
                onClick={() => setAddModalVisible(true)}
              >
                Add Invoice
              </Button>
            </Space>
          </Layout>
          <Divider />
          <div style={{marginLeft: '1rem', marginRight: '1rem'}}>
            <Row justify="space-between">
                {
                  test.map(t => (
                    <Col key={t.id + Math.random()} span={8}>
                      <InvoiceCard invoice={t}/>
                    </Col>
                  ))
                }
            </Row>
          </div>
          <Modal 
            title="Add new invoice" 
            visible={addModalVisible}
            onCancel={() => setAddModalVisible(false)}
          >
              <Title>
                Huutista
              </Title>
          </Modal>
        </Content>
      </Layout>
    </>
  )
}

interface IProps {
  api: string,
}

export const getServerSideProps: GetServerSideProps = async (context) => {
  return {
    props: {
      api: API_URL
    }
  }
}

export default Home
